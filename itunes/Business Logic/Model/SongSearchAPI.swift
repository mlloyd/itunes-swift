import UIKit
import ObjectMapper

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
class SongSearchAPI: Mappable {

    var count: NSNumber?
    var songs: [Song]?

    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    required init?(_ map: Map){
        
    }
    
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    func mapping(map: Map) {
        self.count <- map["resultCount"]
        self.songs <- map["results"]
    }
    
}
